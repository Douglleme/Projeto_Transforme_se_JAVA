/* ) Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar 
Algoritmos!”.  */
public class Ex01 {
   public static void main(String[] args) {
    		
	int i = 1;
	while (i<=20) {
		System.out.println(i + " Eu gosto de estudar Algoritmos!!!");
		i++;
	}
}

}